    def _fill_missing_choices_per_mask(self):
        MARK_BY_PUNKTUM = {
            '8': '—', '18': '—', '2100': '—',
            '19': ':', '2101': ':',
            '2102': ',',
            '2103': '«»',
            '2': ',', '3': ',', '4': ',', '6': ',', '7': ',',
            '11': ',', '12': ',', '13': ',', '14': ',', '15': ',',
        }
        count = 0
        for ex in OgePunktumExample.objects.filter(choices_per_mask=''):
            mark = MARK_BY_PUNKTUM.get(ex.punktum_id)
            if not mark:
                continue
            mask_count = ex.masked_word.count(f'*{ex.punktum_id}*')
            if mask_count:
                ex.choices_per_mask = '|'.join([mark] * mask_count)
                ex.save(update_fields=['choices_per_mask'])
                count += 1
        self.stdout.write(f'  Filled choices_per_mask for {count} existing examples')

